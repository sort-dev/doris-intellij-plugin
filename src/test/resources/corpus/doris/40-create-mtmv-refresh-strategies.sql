CREATE MATERIALIZED VIEW acme_daily_rollup_mv
BUILD IMMEDIATE
REFRESH COMPLETE
ON SCHEDULE EVERY 1 HOUR STARTS '2026-07-08 00:00:00'
DISTRIBUTED BY HASH(user_id) BUCKETS 4
PROPERTIES ("replication_num" = "1")
AS SELECT user_id, COUNT(*) AS cnt FROM acme_events GROUP BY user_id;
CREATE MATERIALIZED VIEW acme_commit_mv
BUILD DEFERRED
REFRESH AUTO
ON COMMIT
DISTRIBUTED BY RANDOM BUCKETS AUTO
AS SELECT user_id FROM acme_events;
